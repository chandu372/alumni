  <h1 style="color: white">About us:</h1>
  <p style="color: white">VNIT VISVESVARAYA NATIONAL INSTITUTE OF TECHNOLOGY (VNITAA) wholeheartedly appreciates all the support and assistance that we have received from all our alumni. We feel proud to share that the network we started with few chapters, has now grown enormously across the globe.We hope you will continuing with your generous support towards the society and encourage your fellow alumni too.</p>
  <p style="color: white">VNIT VISVESVARAYA NATIONAL INSTITUTE OF TECHNOLOGY (VNITAA) creates and maintains a life-long connection between VNIT Nagpur and its alumni. In collaboration with an extremely dedicated volunteer Board of Directors, the Alumni Association works to connect alumni, support students, and build an unforgettable Institute experience through a diversity of events, programmes and services. With registered life members, VNITAA has worked diligently to serve its alumni and alma mater, as well as a host of related constituencies. </p>
  <br>
  <h1 style="color: white">Our Mission:</h1>
 <p style="color: white">The mission of the Association is to foster strong bonds between alumni, students and the Institute, to keep alumni informed, and create a network enabling them to remain engaged with their alma mater and help shape its future through the Association’s programmes and services. </p>  
