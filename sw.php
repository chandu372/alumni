<h3 style="color: white">Alumni Donation-Corpus Fund</h3>
<p style="color: white">Thank you dear Alumnus/Alumna for expressing your interest in contributing towards the Alumni Fund for the welfare of Economically Challenged Students of VNIT.</p>
<p style="color: white">The amount you pledge will be eligible for tax incentives section 80 G of Income Tax.</p>
<p style="color: white">These benefits nonetheless are frivolous as compared to the countless “thanks” that you’ll receive from the students who shall benefit from your contributions. We humbly request you to choose one of the options below and pledge as much as you can contribute to the Alumni Fund. </p>
<p style="color: white">There are Four Options to pay and contribute for the Alumni Fund-raising Initiative- One Time (Standard Amounts), One Time (Custom Amounts), Yearly Subscription & Monthly Subscription.</p>
<p style="color: white">You can read about all the Scholarships & VNITAA Fund raising initiative, its success and the press coverage received and how your contribution can only enhance this initiative while giving you tax incentives and an unparalleled satisfaction of having given back to your Alma Mater.</p>
