</p>
<h2 style="color: white">One Time – Standard Amounts</h2>
<p style="color: white">Choosing this option you’ll have to select between the four amounts of Rs 5,000/-, Rs 10,000/-, Rs 15,000/- & Rs 20,000/- and click on the button accordingly to proceed and make a one time payment of that amount.
</p>
<h2 style="color: white">One Time – Custom Amounts</h2>
<p style="color: white">Choosing this option you can proceed to enter a custom amount (anything between Rs 1,000/- to Rs 1,000,000/-) as it suits your plan and willingness to contribute to the VNITAA Alumni Fund</p>
<h2 style="color: white">Monthly Subscription</h2>
<p style="color: white">Choosing this option you can proceed to subscribe to pay a monthly amount (Rs 500/-, Rs 1,000/-, Rs 1,500/- or Rs 2,000/-) as it suits your plan and willingness to contribute to the VNITAA Alumni Fund</p>
<h2 style="color: white">Yearly Subscription</h2>
<p style="color: white">Choosing this option you can proceed to subscribe to pay a Yearly amount (Rs 1,000/-, Rs 2,500/-, Rs 5,000/- or Rs 10,000/-) as it suits your plan and willingness to contribute to the VNITAA Alumni Fund
</p>
